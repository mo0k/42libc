#include "libft.h"

size_t		ft_nbrlen(int nbr)
{
	int		len;

	len = 0;
	if (nbr < 0)
		len++;
	while (nbr / 10 != 0)
	{
		len++;
		nbr /= 10;
	}
	len++;
	return (len);
}