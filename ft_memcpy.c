#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char *ptr_dst;
	char *ptr_src;

	ptr_dst = (char *)dst;
	ptr_src = (char *)src;
	while (n--)
		*(ptr_dst++) = *(ptr_src++);
	return (dst);
}
