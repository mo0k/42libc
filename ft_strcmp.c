int		ft_strcmp(char *s1, char *s2)
{
	int	ct;

	ct = 0;
	while ((s1[ct] || s2[ct]) && (s1[ct] == s2[ct]))
	{
		if (s1[ct] < s2[ct])
			return (s1[ct] - s2[ct]);
		if (s1[ct] > s2[ct])
			return (s1[ct] - s2[ct]);
		ct++;
	}
	return (s1[ct] - s2[ct]);
}
